SOURCE = Path(os.environ["SOURCE_DOCX"])
OUTPUT = Path(os.environ["OUTPUT_DOCX"])


def insert_paragraph_after(paragraph, text="", style=None):
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    new_para = paragraph._parent.add_paragraph()
    new_para._p = new_p
    if style:
        new_para.style = style
    if text:
        new_para.add_run(text)
    return new_para


def set_spacing(paragraph):
    pf = paragraph.paragraph_format
    pf.space_before = Pt(3)
    pf.space_after = Pt(3)
    pf.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE


def set_heading_spacing(paragraph):
    pf = paragraph.paragraph_format
    pf.space_before = Pt(9)
    pf.space_after = Pt(4)


def add_after(anchor, entries):
    current = anchor
    for style, text in entries:
        current = insert_paragraph_after(current, text, style)
        if style and "Heading" in style:
            set_heading_spacing(current)
        else:
            set_spacing(current)
    return current


def mark_font(doc):
    for paragraph in doc.paragraphs:
        for run in paragraph.runs:
            if run.text:
                run.font.name = "宋体"
                run._element.get_or_add_rPr().rFonts.set(qn("w:eastAsia"), "宋体")


def paragraph_has_text(paragraph, target):
    return "".join(paragraph.text.split()) == "".join(target.split())


