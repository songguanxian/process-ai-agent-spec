def insert_paragraph_after(paragraph, text="", style=None):
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    new_para = paragraph._parent.add_paragraph()
    new_para._p = new_p
    if style:
        new_para.style = style
    if text:
        new_para.add_run(text)
    return new_para


def set_spacing(paragraph):
    pf = paragraph.paragraph_format
    pf.space_before = Pt(3)
    pf.space_after = Pt(3)
    pf.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE


def set_heading_spacing(paragraph):
    pf = paragraph.paragraph_format
    pf.space_before = Pt(9)
    pf.space_after = Pt(4)


def add_after(anchor, entries):
    current = anchor
    for style, text in entries:
        current = insert_paragraph_after(current, text, style)
        if style and "Heading" in style:
            set_heading_spacing(current)
        else:
            set_spacing(current)
    return current


def normalize(text):
    return "".join((text or "").split())


def replace_in_paragraph(paragraph, old, new):
    if old not in paragraph.text:
        return False
    updated = paragraph.text.replace(old, new)
    if paragraph.runs:
        paragraph.runs[0].text = updated
        for run in paragraph.runs[1:]:
            run.text = ""
    else:
        paragraph.add_run(updated)
    return True


def replace_everywhere(doc, old, new):
    count = 0
    for paragraph in doc.paragraphs:
        if replace_in_paragraph(paragraph, old, new):
            count += 1
    for table in doc.tables:
        for row in table.rows:
            for cell in row.cells:
                for paragraph in cell.paragraphs:
                    if replace_in_paragraph(paragraph, old, new):
                        count += 1
    return count


def mark_font(doc):
    for paragraph in doc.paragraphs:
        for run in paragraph.runs:
            if run.text:
                run.font.name = "宋体"
                run._element.get_or_add_rPr().rFonts.set(qn("w:eastAsia"), "宋体")


def is_empty_tail_paragraph(paragraph):
    if paragraph.text.strip():
        return False
    el = paragraph._element
    if el.xpath(".//w:drawing") or el.xpath(".//w:pict"):
        return False
    if el.xpath(".//w:br[@w:type='page']") or el.xpath(".//w:sectPr"):
        return False
    return True


def cleanup_trailing_blanks(doc):
    for paragraph in reversed(doc.paragraphs):
        if not is_empty_tail_paragraph(paragraph):
            break
        paragraph._element.getparent().remove(paragraph._element)


